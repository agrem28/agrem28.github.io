function drawChessboard(size) {
  var chessboard = [];
  for (let i = 0; i < size; i++) {
    chessboard[i] = "";
    for (let j = 0; j < size; j++) {
      if (j % 2 === 0) {
        if (i % 2 === 0) {
          chessboard[i] += " ";
        } else {
          chessboard[i] += "#";
        }
      } else {
        if (i % 2 === 0) {
          chessboard[i] += "#";
        } else {
          chessboard[i] += " ";
        }
      }
    }
  }
  chessboard[chessboard.length] = "";
  console.log(chessboard.join("\n"));
}

